// Data Types (Primitive Data Types)

// 1. Nummbers: represents numeric value
let age = 25;
console.log("The value of age is: ", age)
console.log("The data type of variable age: ", typeof age)

// 2. Strings - represent textual / character values (e.g. Name, address)
let name = "Geomarie";
console.log("The value of name: ", name);
console.log("The datatype of variable name: ", typeof name);

// 3. Boolean: true or false
let isStudent = true;
let isTeacher = false;
console.log("The value of isStudent: ", isStudent);
console.log("The datatype of variable isStudent: ", typeof isStudent)

// Null - represent intentional absence of value
let car = null;
console.log("The value of car: ", car);
console.log("The datatype of variable car: ", typeof car);

// Undefined - represent unintentional absence of value
let city;
console.log("The value of city: ", city);
console.log("The datatype of variable city: ", typeof city);